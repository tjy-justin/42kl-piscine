#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putchar(nb % 10 + '0');
	}
	else
		ft_putchar(nb + '0');
}

#include <stdio.h>

int	main(void)
{
	ft_putnbr(123);
	printf("\n");
	ft_putnbr(12);
	printf("\n");
	ft_putnbr(-2147483648);
	printf("\n");
	return (0);
}
