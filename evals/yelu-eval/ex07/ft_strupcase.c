#include <stdio.h>
#include <string.h>

char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 96 && str[i] < 123)
		{
			str[i] = str[i] - 32;
		}
		i++;
	}
	return (str);
}

int	main(void)
{
	char	upper;

	upper[11] = "aHaHaHaHaHa";
	printf("Original: %s\n", upper);
	printf("All uppercase: %s\n", ft_strupcase(upper));
}

